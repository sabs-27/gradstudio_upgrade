/**
 * AUTOMATED UPLOAD SCRIPT
 * File: scripts/upload-simulations.js
 * 
 * Usage: node scripts/upload-simulations.js --course aws --path ./new-simulations/
 */

import { readdir, readFile } from 'fs/promises';
import { join, basename } from 'path';
import { parse } from 'node-html-parser';
import { nanoid } from 'nanoid';

// ===== CONFIGURATION =====
const CONFIG = {
  // R2 bucket name (matches wrangler.toml)
  r2Bucket: 'learning-simulations',
  
  // D1 database name
  d1Database: 'learning-platform-db',
  
  // File patterns to upload
  filePatterns: ['.html', '.htm'],
  
  // Metadata extraction rules
  metadataSelectors: {
    title: ['meta[name="title"]', 'title', 'h1'],
    description: ['meta[name="description"]', 'meta[property="og:description"]'],
    keywords: ['meta[name="keywords"]']
  }
};

// ===== METADATA EXTRACTOR =====

/**
 * Extract metadata from HTML file
 */
function extractMetadata(htmlContent, filename) {
  const root = parse(htmlContent);
  
  // Extract title
  let title = null;
  for (const selector of CONFIG.metadataSelectors.title) {
    const el = root.querySelector(selector);
    if (el) {
      title = selector.startsWith('meta') 
        ? el.getAttribute('content')
        : el.textContent.trim();
      if (title) break;
    }
  }
  
  // Extract description
  let description = null;
  for (const selector of CONFIG.metadataSelectors.description) {
    const el = root.querySelector(selector);
    if (el) {
      description = el.getAttribute('content');
      if (description) break;
    }
  }
  
  // Extract keywords/tags
  let tags = [];
  for (const selector of CONFIG.metadataSelectors.keywords) {
    const el = root.querySelector(selector);
    if (el) {
      const content = el.getAttribute('content');
      if (content) {
        tags = content.split(',').map(t => t.trim().toLowerCase());
        break;
      }
    }
  }
  
  // Generate slug from filename
  const slug = basename(filename, '.html')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
  
  // Check if interactive simulation (has certain HTML elements)
  const hasSimulation = !!(
    root.querySelector('canvas') ||
    root.querySelector('script[src*="three"]') ||
    root.querySelector('script[src*="p5"]') ||
    htmlContent.includes('interactive') ||
    htmlContent.includes('simulation')
  );
  
  return {
    title: title || slug.replace(/-/g, ' ').replace(/\b\w/g, l => l.toUpperCase()),
    description: description || '',
    slug,
    tags,
    hasSimulation,
    searchText: `${title} ${description} ${tags.join(' ')}`.toLowerCase()
  };
}

// ===== FILE PROCESSOR =====

/**
 * Process a single simulation file
 */
async function processSimulation(filePath, courseId, sectionId) {
  console.log(`Processing: ${filePath}`);
  
  try {
    // Read HTML file
    const htmlContent = await readFile(filePath, 'utf-8');
    
    // Extract metadata
    const metadata = extractMetadata(htmlContent, filePath);
    
    // Generate unique ID
    const simId = `sim-${courseId}-${nanoid(10)}`;
    
    // Build R2 path: simulations/aws/ec2-basics/index.html
    const fileName = basename(filePath);
    const r2Path = `simulations/${courseId}/${metadata.slug}/${fileName}`;
    
    return {
      id: simId,
      sectionId,
      courseId,
      slug: metadata.slug,
      title: metadata.title,
      description: metadata.description,
      filePath: r2Path,
      hasSimulation: metadata.hasSimulation,
      tags: metadata.tags,
      searchText: metadata.searchText,
      localPath: filePath
    };
    
  } catch (error) {
    console.error(`Error processing ${filePath}:`, error.message);
    return null;
  }
}

// ===== BATCH UPLOADER =====

/**
 * Upload simulations to R2 using wrangler
 */
async function uploadToR2(simulations) {
  console.log(`\nUploading ${simulations.length} files to R2...`);
  
  const { exec } = await import('child_process');
  const { promisify } = await import('util');
  const execAsync = promisify(exec);
  
  let uploaded = 0;
  let failed = 0;
  
  for (const sim of simulations) {
    try {
      const cmd = `wrangler r2 object put ${CONFIG.r2Bucket}/${sim.filePath} --file="${sim.localPath}"`;
      await execAsync(cmd);
      console.log(`✓ Uploaded: ${sim.filePath}`);
      uploaded++;
    } catch (error) {
      console.error(`✗ Failed to upload ${sim.filePath}:`, error.message);
      failed++;
    }
  }
  
  return { uploaded, failed };
}

/**
 * Insert simulations into D1 database
 */
async function insertIntoDB(simulations) {
  console.log(`\nInserting ${simulations.length} records into D1...`);
  
  const { exec } = await import('child_process');
  const { promisify } = await import('util');
  const { writeFile } = await import('fs/promises');
  const execAsync = promisify(exec);
  
  // Generate SQL INSERT statements
  let sql = '-- Auto-generated by upload script\n\n';
  
  simulations.forEach((sim, idx) => {
    sql += `INSERT INTO simulations (
      id, section_id, course_id, slug, title, 
      description, icon_class, file_path, 
      has_simulation, display_order, search_text, upload_date
    ) VALUES (
      '${sim.id}',
      '${sim.sectionId}',
      '${sim.courseId}',
      '${sim.slug}',
      '${sim.title.replace(/'/g, "''")}',
      '${sim.description.replace(/'/g, "''")}',
      'fa-file',
      '${sim.filePath}',
      ${sim.hasSimulation ? 1 : 0},
      ${idx + 1},
      '${sim.searchText.replace(/'/g, "''")}',
      date('now')
    );\n\n`;
    
    // Insert tags
    sim.tags.forEach(tag => {
      sql += `INSERT INTO tags (simulation_id, tag_name) VALUES ('${sim.id}', '${tag}');\n`;
    });
    sql += '\n';
    
    // Initialize statistics
    sql += `INSERT INTO statistics (simulation_id, views, likes) VALUES ('${sim.id}', 0, 0);\n\n`;
  });
  
  // Update metadata
  sql += `UPDATE metadata SET value = (SELECT COUNT(*) FROM simulations), updated_at = datetime('now') WHERE key = 'total_simulations';\n`;
  sql += `UPDATE metadata SET value = date('now'), updated_at = datetime('now') WHERE key = 'last_upload_date';\n`;
  
  // Write to temp file
  const tempFile = `/tmp/upload-${Date.now()}.sql`;
  await writeFile(tempFile, sql);
  
  try {
    // Execute SQL
    await execAsync(`wrangler d1 execute ${CONFIG.d1Database} --file="${tempFile}"`);
    console.log('✓ Database updated successfully');
    return true;
  } catch (error) {
    console.error('✗ Database update failed:', error.message);
    console.error('SQL file saved at:', tempFile);
    return false;
  }
}

// ===== MAIN PIPELINE =====

/**
 * Main upload pipeline
 */
async function uploadPipeline(options) {
  const { coursePath, courseId, sectionId } = options;
  
  console.log('='.repeat(60));
  console.log('SIMULATION UPLOAD PIPELINE');
  console.log('='.repeat(60));
  console.log(`Course: ${courseId}`);
  console.log(`Section: ${sectionId}`);
  console.log(`Path: ${coursePath}`);
  console.log('='.repeat(60));
  
  try {
    // 1. Scan directory for HTML files
    console.log('\n[1/4] Scanning directory...');
    const files = await readdir(coursePath);
    const htmlFiles = files.filter(f => 
      CONFIG.filePatterns.some(ext => f.endsWith(ext))
    );
    
    console.log(`Found ${htmlFiles.length} HTML files`);
    
    if (htmlFiles.length === 0) {
      console.log('No HTML files found. Exiting.');
      return;
    }
    
    // 2. Process all files
    console.log('\n[2/4] Processing files...');
    const simulations = [];
    
    for (const file of htmlFiles) {
      const filePath = join(coursePath, file);
      const sim = await processSimulation(filePath, courseId, sectionId);
      if (sim) simulations.push(sim);
    }
    
    console.log(`Processed ${simulations.length} simulations`);
    
    if (simulations.length === 0) {
      console.log('No valid simulations to upload. Exiting.');
      return;
    }
    
    // 3. Upload to R2
    console.log('\n[3/4] Uploading to R2...');
    const uploadResult = await uploadToR2(simulations);
    console.log(`Uploaded: ${uploadResult.uploaded}, Failed: ${uploadResult.failed}`);
    
    // 4. Insert into database
    console.log('\n[4/4] Updating database...');
    const dbResult = await insertIntoDB(simulations);
    
    // Summary
    console.log('\n' + '='.repeat(60));
    console.log('UPLOAD COMPLETE');
    console.log('='.repeat(60));
    console.log(`✓ Files processed: ${simulations.length}`);
    console.log(`✓ Uploaded to R2: ${uploadResult.uploaded}`);
    console.log(`✓ Database updated: ${dbResult ? 'Yes' : 'No'}`);
    console.log('='.repeat(60));
    
    // Show sample records
    console.log('\nSample records:');
    simulations.slice(0, 3).forEach(sim => {
      console.log(`  - ${sim.title} (${sim.slug})`);
      console.log(`    Tags: ${sim.tags.join(', ') || 'none'}`);
      console.log(`    Interactive: ${sim.hasSimulation ? 'Yes' : 'No'}`);
    });
    
  } catch (error) {
    console.error('\nFATAL ERROR:', error);
    process.exit(1);
  }
}

// ===== CLI INTERFACE =====

async function main() {
  const args = process.argv.slice(2);
  
  // Parse arguments
  let coursePath = null;
  let courseId = null;
  let sectionId = null;
  
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--path' && args[i + 1]) {
      coursePath = args[i + 1];
    }
    if (args[i] === '--course' && args[i + 1]) {
      courseId = args[i + 1];
    }
    if (args[i] === '--section' && args[i + 1]) {
      sectionId = args[i + 1];
    }
  }
  
  // Validate
  if (!coursePath || !courseId || !sectionId) {
    console.log(`
Usage: node scripts/upload-simulations.js --course <id> --section <id> --path <directory>

Example:
  node scripts/upload-simulations.js \\
    --course aws \\
    --section aws-sec-003 \\
    --path ./simulations/aws/compute/

Options:
  --course    Course ID (e.g., 'aws', 'sql')
  --section   Section ID from database (e.g., 'aws-sec-003')
  --path      Directory containing HTML files to upload

Note: Make sure the section exists in your D1 database before running.
    `);
    process.exit(1);
  }
  
  // Run pipeline
  await uploadPipeline({ coursePath, courseId, sectionId });
}

// Run if called directly
if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}

export { uploadPipeline, processSimulation, extractMetadata };
